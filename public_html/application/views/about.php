                <h1>Video Library</h1>
                <p>Video Library was a publicly traded video rental shop based in San Diego, California. It had 80 franchises from 1985 through 1989 before they were acquired and converted into Blockbuster Video in 1989.</p>                
            </div>        
            
            <div class="about">
<p>                Video Library made its initial public stock offering in mid-1985.</p>
<p>Video Library was owned by President and Chief Executive Barry L. Rosenblatt, Chairman Roy Keith Black, and shareholder Pauline Sussman. As part of the acquisition deal, the three received Blockbuster stock valued at $66.4 million. At the time of the acquisition, Blockbuster Video operated 133 company-owned and franchised Blockbuster Video Superstores in 23 states.</p>
<p>Video Library reported a profit of $11.9 million in revenue for the third quarter of Sept. 30, 1987.</p>
